package org.zigmoi.ketchup.test.git;

public class MDistributionManagementParams {
    private String repositoryId;
    private String repositoryUrl;
    private String repositoryName;
    private String snapshotRepositoryId;
    private String snapshotRepositoryUrl;
    private String snapshotRepositoryName;

    public String getRepositoryId() {
        return repositoryId;
    }

    public void setRepositoryId(String repositoryId) {
        this.repositoryId = repositoryId;
    }

    public String getRepositoryUrl() {
        return repositoryUrl;
    }

    public void setRepositoryUrl(String repositoryUrl) {
        this.repositoryUrl = repositoryUrl;
    }

    public String getRepositoryName() {
        return repositoryName;
    }

    public void setRepositoryName(String repositoryName) {
        this.repositoryName = repositoryName;
    }

    public String getSnapshotRepositoryId() {
        return snapshotRepositoryId;
    }

    public void setSnapshotRepositoryId(String snapshotRepositoryId) {
        this.snapshotRepositoryId = snapshotRepositoryId;
    }

    public String getSnapshotRepositoryUrl() {
        return snapshotRepositoryUrl;
    }

    public void setSnapshotRepositoryUrl(String snapshotRepositoryUrl) {
        this.snapshotRepositoryUrl = snapshotRepositoryUrl;
    }

    public String getSnapshotRepositoryName() {
        return snapshotRepositoryName;
    }

    public void setSnapshotRepositoryName(String snapshotRepositoryName) {
        this.snapshotRepositoryName = snapshotRepositoryName;
    }
}
